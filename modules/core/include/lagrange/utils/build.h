    #define LAGRANGE_TARGET_OS_PRIVATE_DEFINITION_LINUX() 0
    #define LAGRANGE_TARGET_OS_PRIVATE_DEFINITION_APPLE() 0
    #define LAGRANGE_TARGET_OS_PRIVATE_DEFINITION_WASM() 0
